public class VariablesDatatypes {
      public static void main(String[] args) {
        System.err.println("***********Variables and DataTypes in JAVA**********");
     
        int age; //declaration
        age = 10; //initialization
        System.out.println(age);
        //or
        // System.out.print(age);
        char letter = 'a'; //always enclosed in single quotes
        System.out.println(letter);
        float height = 5.5f; //always ends with f
        System.out.println(height);
        double value = 445.88d;
        System.out.println(value);
        long big = 6668887666666666668L; //always ends with L
        System.out.println(big);
        
    }
}

 